print("🤖 ELIZA: Olá! Fale comigo. (digite 'sair' para encerrar)\n")

while True:
    entrada = input("Você: ")

    if entrada.lower() == "sair":
        print("🤖 ELIZA: Até mais! Cuide-se.")
        break

    print("🤖 ELIZA:", responder(entrada))