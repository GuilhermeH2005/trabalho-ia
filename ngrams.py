print("Digite uma palavra inicial (ou 'sair'):\n")

while True:
    entrada = input("Você: ")

    if entrada == "sair":
        break

    if entrada not in bigramas:
        print("Palavra desconhecida, tente outra.")
    else:
        print("IA:", gerar_frase_interativa(bigramas, entrada))
