def gerar_frase_interativa(bigramas, palavra_inicial, tamanho=6):
    palavra = palavra_inicial.lower()
    frase = [palavra]

    for _ in range(tamanho - 1):
        if palavra in bigramas:
            palavra = random.choice(bigramas[palavra])
            frase.append(palavra)
        else:
            break

    return " ".join(frase)


